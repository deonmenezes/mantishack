#[derive(Debug, Clone, Error, PartialEq, Eq)]
pub enum TierError {
    #[error("LLM call failed: {0}")]
    Llm(String),
    #[error("sandbox execution failed: {0}")]
    Sandbox(String),
    #[error("hard-tier budget exhausted after {iterations} iterations")]
    BudgetExhausted { iterations: u32 },
    #[error("invalid script returned by LLM: {0}")]
    InvalidScript(String),
}

/// What we're asking the tiered runner to do for a given target +
/// surface. The light tier reads this to pick a primitive; the
/// medium tier feeds it into the LLM prompt.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct Probe {
    pub target_url: String,
    /// Free-form description of what we're testing for. The LLM
    /// reads this verbatim. Operator-supplied or generated by the
    /// hypothesis layer. Examples:
    /// - "IDOR on /api/users?userId=<id>"
    /// - "Mass-assignment via PATCH /rest/v1/users"
    /// - "Unauthenticated access to /admin"
    pub objective: String,
    /// Optional auth context. Light tier consults; medium/hard tier
    /// pass to the LLM so it can write authed requests.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub attacker_profile: Option<mantis_auth::AuthProfile>,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub victim_profile: Option<mantis_auth::AuthProfile>,
    /// Max wall-clock seconds for the full escalation chain.
    pub budget_seconds: u32,
}

/// A finding produced by ANY tier. Same shape regardless of
/// whether it came from a Rust primitive or an LLM-written script.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct TieredFinding {
    pub tier: TierKind,
    pub vuln_class: String,
    pub severity: String,
    pub url: String,
    pub evidence: String,
    /// When tier == Medium/Hard, this is the LLM-generated script
    /// that produced the finding. None for light tier.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub script: Option<String>,
    /// When tier == Hard, the verifier's verdict ID + reasoning.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub verifier_verdict: Option<String>,
    /// Number of iterations the hard-tier verifier took to converge
    /// (1 = first attempt accepted).
    #[serde(default)]
    pub hard_iterations: u32,
}
