//! Three-tier escalating execution model.
//!
//! Mantis tries cheap, fixed Rust primitives first. When they miss,
//! it escalates: an LLM writes a one-shot exploit script tailored
//! to the surface; Mantis runs it sandboxed. If THAT misses, the
//! hard tier opens a verifier loop where the LLM iterates on the
//! script until the verifier accepts a finding (or budget exhausts).
//!
//! ```text
//! ┌───────────┐   miss   ┌────────────┐   miss   ┌──────────────┐
//! │   light   │ ────────▶│   medium   │ ────────▶│ hard verifier│
//! │ (Rust)    │          │ (LLM gen)  │          │ (LLM iterate)│
//! └───────────┘  hit ↓   └────────────┘  hit ↓   └──────────────┘
//!                Finding                  Finding         Finding
//! ```
//!
//! Each tier is a [`Tier`] implementor. A [`TieredRunner`] dispatches
//! light → medium → hard in order, short-circuiting on the first
//! finding. The medium and hard tiers depend on an [`LlmCodegen`]
//! trait which the orchestrator wires to whatever LLM provider is
//! configured (`mantis llm probe --provider anthropic` style).

